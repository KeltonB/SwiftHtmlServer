func parse(_ s:String) -> [String]{
    var tp = s.characters.map{ String($0) }
    var args:[String] = ["","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","","",""]
    let len = tp.count
    var arglen:Int
    var i = 0
    var ars = 0
    while(true){
        if i == len{break}
        arglen = args.count
        if arglen == ars{break}
        if tp[i] == " "{
            ars += 1
        }
        if args.count == 0{
            args[0] = tp[i].replacingOccurrences(of: " ", with: "")
        }else{
            args[ars] = args[ars] + tp[i].replacingOccurrences(of: " ", with: "")
            i += 1
        }
    }
    
    return args
    
}
